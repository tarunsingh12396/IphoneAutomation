package Pojo;

public class CompanyPFdetails {

	public String pfname;
	public String pfcity;
	public String pfyear;
	public String noOfEmployees;
	public String getPfname() {
		return pfname;
	}
	public void setPfname(String pfname) {
		this.pfname = pfname;
	}
	public String getPfcity() {
		return pfcity;
	}
	public void setPfcity(String pfcity) {
		this.pfcity = pfcity;
	}
	public String getPfyear() {
		return pfyear;
	}
	public void setPfyear(String pfyear) {
		this.pfyear = pfyear;
	}
	public String getNoOfEmployees() {
		return noOfEmployees;
	}
	public void setNoOfEmployees(String noOfEmployees) {
		this.noOfEmployees = noOfEmployees;
	}
	
	
	
	

}
